const LocaleContext = createContext<LocaleContextType | undefined>(undefined);

export const LocaleProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState<Language>(() => {
    const saved = localStorage.getItem('deesse-language');
    return (saved as Language) || 'fr';
  });
  const [currency, setCurrency] = useState<Currency>(() => {
    const saved = localStorage.getItem('deesse-currency');
    return (saved as Currency) || 'EUR';
  });

  useEffect(() => {
    localStorage.setItem('deesse-language', language);
  }, [language]);

  useEffect(() => {
    localStorage.setItem('deesse-currency', currency);
  }, [currency]);

  const t = (key: string): string => {
    return translations[language][key] || key;
  };

  const formatPrice = (price: number): string => {
    const rate = currency === 'USD' ? 1.08 : 1;
    const converted = price * rate;
    return new Intl.NumberFormat(language === 'en' ? 'en-US' : language, {
      style: 'currency',
      currency: currency,
    }).format(converted);
  };

  return (
    <LocaleContext.Provider value={{ language, currency, setLanguage, setCurrency, t, formatPrice }}>
      {children}
    </LocaleContext.Provider>
  );
};

export const useLocale = () => {
  const context = useContext(LocaleContext);
  if (!context) {
    throw new Error('useLocale must be used within a LocaleProvider');
  }
  return context;
};

export const languages: Language[] = ['fr', 'en', 'de', 'es', 'pt', 'it', 'nl', 'ja', 'ko'];
export const currencies: Currency[] = ['EUR', 'USD'];
